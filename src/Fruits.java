public class Fruits {
    String fruitName;
    String color;

    public Fruits(String fruitName, String color) {
        this.fruitName = fruitName;
        this.color = color;
    }

    public String getFruitName() {
        return fruitName;
    }

    public void setFruitName(String fruitName) {
        this.fruitName = fruitName;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }
}
